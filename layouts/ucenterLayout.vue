import cookie from "js-cookie";
import loginApi from "@/api/login";

export default {
  data() {
    return {
      token: "",
      loginInfo: {
        id: "",
        age: "",
        avatar: "",
        mobile: "",
        nickname: "",
        sex: ""
      }
    };
  },
  methods: {
    //创建方法,从cookie获取用户信息
    showInfo() {
      //从cookie获取用户信息
      var userStr = cookie.get("underdogedu_ucenter");
      //console.log(userStr)
      //把字符串转换成json对象(js对象)
      if (userStr) {
        this.loginInfo = JSON.parse(userStr);
      }
    },
    logout() {
      cookie.set("underdogedu_token", "", { domain: "localhost" });
      cookie.set("underdogedu_ucenter", "", { domain: "localhost" });
      window.location.href = "/";
    },
    //微信登录显示的方法
    wxLogin() {
      //把token值放到cookie里面
      cookie.set("underdogedu_token", this.token, { domain: "localhost" });
      cookie.set("underdogedu_ucenter", "", { domain: "localhost" });
      //调用接口，根据token值获取用户信息
      loginApi.getLoginUserInfo().then(response => {
        this.loginInfo = response.data.data.userInfo;
        cookie.set("underdogedu_ucenter", this.loginInfo, {
          domain: "localhost"
        });
      });
    }
  },
  created() {
    this.token = this.$route.query.token;
    if (this.token) {
      //判断路径中是否有token值
      this.wxLogin();
    }
    this.showInfo();
    this.$store.commit('initUserInfo',this.loginInfo)
  }
};
